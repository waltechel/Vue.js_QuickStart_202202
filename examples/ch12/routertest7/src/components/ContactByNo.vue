<template>
  <div>
    <hr class="divider" />
    <div>
      <table class="detail table table-bordered">
        <tbody>
          <tr class="active">
            <td>일련번호</td>
            <td>{{contact.no}}</td>
          </tr>
          <tr class="active">
            <td>이름</td>
            <td>{{contact.name}}</td>
          </tr>
          <tr class="active">
            <td>전화</td>
            <td>{{contact.tel}}</td>
          </tr>
          <tr class="active">
            <td>주소</td>
            <td>{{contact.address}}</td>
          </tr>
        </tbody>
      </table>
    </div>
  </div>
</template>

<script>
import contactlist from "../ContactList";
export default {
  name: "contactbyno",
  // no 속성에 라우팅을 하면서 값을 전달해준다.
  props: ["no"],
  data() {
    return {
      contacts: contactlist.contacts
    };
  },
  computed: {
    contact() {
      return this.contacts.find(item => item.no === parseInt(this.no, 10));
    }
  }
};
</script>

